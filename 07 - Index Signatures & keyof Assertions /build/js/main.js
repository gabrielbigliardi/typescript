"use strict";
// Index Signatures 
// Uteis quando vc cria um objeto mas nao sabe as keys 
// mas sabe o formato do objeto
// ts requires index signatures if you attempt to access an object property dynamically
const todaysTransactions = {
    Pizza: -10,
    Books: -5,
    Job: 50,
};
console.log(todaysTransactions.Pizza);
console.log(todaysTransactions['Pizza']);
let prop = 'Pizza';
console.log(todaysTransactions[prop]); // so eh possivel com o index signature
const todaysNet = (transactions) => {
    let total = 0;
    for (const transaction in transactions) {
        total += transactions[transaction];
    }
    return total;
};
console.log(todaysNet(todaysTransactions));
